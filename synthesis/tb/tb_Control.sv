`timescale 1ns/1ps

module tb_Control;

    //----------------------------------------------------------------------
    // DUT Signals
    //----------------------------------------------------------------------

    logic clock;
    logic reset;

    IS::op_code_t opcode;
    logic [2:0] funct3;
    logic [6:0] funct7;

    logic zero;
    logic negative;

    logic [CTRL::ENDctrl:0] ctrl_signal;
    logic [1:0] Immtype;
    logic [1:0] ALUsrcA;
    logic [1:0] ALUsrcB;
    CTRL::alu_op_t ALUctrl;
    logic [1:0] RETsrc;
    CTRL::data_size_t MEMdata;

    //----------------------------------------------------------------------
    // DUT
    //----------------------------------------------------------------------

    Control dut(
        .clock(clock),
        .reset(reset),
        .opcode(opcode),
        .funct3(funct3),
        .funct7(funct7),
        .zero(zero),
        .negative(negative),

        .ctrl_signal(ctrl_signal),
        .Immtype(Immtype),
        .ALUsrcA(ALUsrcA),
        .ALUsrcB(ALUsrcB),
        .ALUctrl(ALUctrl),
        .RETsrc(RETsrc),
        .MEMdata(MEMdata)
    );

    //----------------------------------------------------------------------
    // Clock
    //----------------------------------------------------------------------

    initial
        clock = 0;

    always #10
        clock = ~clock;

    //----------------------------------------------------------------------
    // Dump
    //----------------------------------------------------------------------

    initial begin
        $dumpfile("tests/Control.vcd");
        $dumpvars(0, tb_Control);

        reset = 0;
    end

    //----------------------------------------------------------------------
    // PASS / FAIL
    //----------------------------------------------------------------------

    integer passed = 0;
    integer failed = 0;

    task automatic check;

        input logic condition;
        input [255:0] msg;

        begin

            #1;

            if(condition) begin
                passed++;
                $display("[PASS] %s", msg);
            end
            else begin
                failed++;
                $display("[FAIL] %s", msg);
            end

        end

    endtask

    //----------------------------------------------------------------------
    // Advance one FSM state
    //----------------------------------------------------------------------

    task automatic step;

        begin

            @(posedge clock);
            #1;

        end

    endtask

    //----------------------------------------------------------------------
    // Reset
    //----------------------------------------------------------------------

    task automatic cpu_reset;

        begin

            reset = 1;
            $display("RESET");
            repeat(2)
                @(posedge clock);

            #1;

            reset = 0;

            #1;

        end

    endtask

    //----------------------------------------------------------------------
    // MAIN
    //----------------------------------------------------------------------

    initial begin

        opcode      = IS::OP;
        funct3      = 3'b000;
        funct7      = 7'b0000000;
        zero        = 0;
        negative    = 0;

        //--------------------------------------------------------------
        // RESET
        //--------------------------------------------------------------

        cpu_reset();

        //--------------------------------------------------------------
        // FETCH
        //--------------------------------------------------------------
       
        check(ctrl_signal[CTRL::PCwrite]  == 1, "FETCH : PCwrite");

        check(ctrl_signal[CTRL::IRwrite]  == 1, "FETCH : IRwrite");

        check(ALUsrcA == 2'b00, "FETCH : ALUsrcA");

        check(ALUsrcB == 2'b10, "FETCH : ALUsrcB");

        check(ALUctrl == CTRL::ADDalu, "FETCH : ADD");

        check(RETsrc == 2'b10, "FETCH : RETsrc");

        //--------------------------------------------------------------
        // DECODE
        //--------------------------------------------------------------

        step();

        check(ctrl_signal[CTRL::PCwrite] == 0,
            "DECODE : PCwrite disabled");

        check(ctrl_signal[CTRL::IRwrite] == 0,
            "DECODE : IRwrite disabled");

        check(ALUsrcA == 2'b01,
            "DECODE : ALUsrcA");

        check(ALUsrcB == 2'b01,
            "DECODE : ALUsrcB");

        //--------------------------------------------------------------
        //==============================================================
        //====================== R TYPE ================================
        //==============================================================
        //--------------------------------------------------------------

        opcode = IS::OP;

        step();

        //--------------------------------------------------------------
        // ADD
        //--------------------------------------------------------------

        funct3 = 3'b000;
        funct7 = 7'b0000000;

        #1;

        check(ALUctrl == CTRL::ADDalu,
            "ADD");

        //--------------------------------------------------------------
        // SUB
        //--------------------------------------------------------------

        funct3 = 3'b000;
        funct7 = 7'b0100000;

        #1;

        check(ALUctrl == CTRL::SUBalu,
            "SUB");

        //--------------------------------------------------------------
        // SLL
        //--------------------------------------------------------------

        funct3 = 3'b001;

        #1;

        check(ALUctrl == CTRL::SLLalu,
            "SLL");

        //--------------------------------------------------------------
        // SLT
        //--------------------------------------------------------------

        funct3 = 3'b010;

        #1;

        check(ALUctrl == CTRL::SUBalu,
            "SLT compare");

        //--------------------------------------------------------------
        // SLTU
        //--------------------------------------------------------------

        funct3 = 3'b011;

        #1;

        check(ALUctrl == CTRL::SUBalu,
            "SLTU compare");

        //--------------------------------------------------------------
        // XOR
        //--------------------------------------------------------------

        funct3 = 3'b100;

        #1;

        check(ALUctrl == CTRL::XORalu,
            "XOR");

        //--------------------------------------------------------------
        // SRL
        //--------------------------------------------------------------

        funct3 = 3'b101;
        funct7 = 7'b0000000;

        #1;

        check(ALUctrl == CTRL::SRLalu,
            "SRL");

        //--------------------------------------------------------------
        // SRA
        //--------------------------------------------------------------

        funct3 = 3'b101;
        funct7 = 7'b0100000;

        #1;

        check(ALUctrl == CTRL::SRAalu,
            "SRA");

        //--------------------------------------------------------------
        // OR
        //--------------------------------------------------------------

        funct3 = 3'b110;

        #1;

        check(ALUctrl == CTRL::ORalu,
            "OR");

        //--------------------------------------------------------------
        // AND
        //--------------------------------------------------------------

        funct3 = 3'b111;

        #1;

        check(ALUctrl == CTRL::ANDalu,
            "AND");

        //--------------------------------------------------------------
        //==============================================================
        //====================== I TYPE ================================
        //==============================================================
        //--------------------------------------------------------------

        cpu_reset();

        opcode = IS::OP_IMM;

        step();
        step();

        //--------------------------------------------------------------
        // ADDI
        //--------------------------------------------------------------

        funct3 = 3'b000;

        #1;

        check(ALUctrl == CTRL::ADDalu,
            "ADDI");

        //--------------------------------------------------------------
        // SLTI
        //--------------------------------------------------------------

        funct3 = 3'b010;

        #1;

        check(ALUctrl == CTRL::SUBalu,
            "SLTI");

        //--------------------------------------------------------------
        // SLTIU
        //--------------------------------------------------------------

        funct3 = 3'b011;

        #1;

        check(ALUctrl == CTRL::SUBalu,
            "SLTIU");

        //--------------------------------------------------------------
        // XORI
        //--------------------------------------------------------------

        funct3 = 3'b100;

        #1;

        check(ALUctrl == CTRL::XORalu,
            "XORI");

        //--------------------------------------------------------------
        // ORI
        //--------------------------------------------------------------

        funct3 = 3'b110;

        #1;

        check(ALUctrl == CTRL::ORalu,
            "ORI");

        //--------------------------------------------------------------
        // ANDI
        //--------------------------------------------------------------

        funct3 = 3'b111;

        #1;

        check(ALUctrl == CTRL::ANDalu,
            "ANDI");

        //--------------------------------------------------------------
        //==============================================================
        //====================== LOAD ================================
        //==============================================================
        //--------------------------------------------------------------

        cpu_reset();

        opcode = IS::LOAD;

        step();
        step();

        check(Immtype == 2'b00,
            "LOAD immediate");

        //--------------------------------------------------------------
        // LB
        //--------------------------------------------------------------
        step();
        funct3 = 3'b000;

        #1;

        check(MEMdata == CTRL::Byte,
            "LB");

        //--------------------------------------------------------------
        // LH
        //--------------------------------------------------------------

        funct3 = 3'b001;

        #1;

        check(MEMdata == CTRL::HalfWord,
            "LH");

        //--------------------------------------------------------------
        // LW
        //--------------------------------------------------------------

        funct3 = 3'b010;

        #1;

        check(MEMdata == CTRL::Word,
            "LW");

        //--------------------------------------------------------------
        // LBU
        //--------------------------------------------------------------

        funct3 = 3'b100;

        #1;

        check(MEMdata == CTRL::ByteUnsigned,
            "LBU");

        //--------------------------------------------------------------
        // LHU
        //--------------------------------------------------------------

        funct3 = 3'b101;

        #1;

        check(MEMdata == CTRL::HalfWordUnsigned,
            "LHU");

        //--------------------------------------------------------------
        //==============================================================
        //====================== STORE ================================
        //==============================================================
        //--------------------------------------------------------------

        cpu_reset();

        opcode = IS::STORE;

        step();
        step();

        check(Immtype == 2'b01,
            "STORE immediate");

        //--------------------------------------------------------------
        // SB
        //--------------------------------------------------------------
        step();
        funct3 = 3'b000;

        #1;

        check(MEMdata == CTRL::Byte,
            "SB");

        //--------------------------------------------------------------
        // SH
        //--------------------------------------------------------------

        funct3 = 3'b001;

        #1;

        check(MEMdata == CTRL::HalfWord,
            "SH");

        //--------------------------------------------------------------
        // SW
        //--------------------------------------------------------------

        funct3 = 3'b010;

        #1;

        check(MEMdata == CTRL::Word,
            "SW");

        //--------------------------------------------------------------
        //==============================================================
        //====================== BRANCH ================================
        //==============================================================
        //--------------------------------------------------------------

        cpu_reset();

        opcode = IS::BRANCH;

        step();
        step();

        check(Immtype == 2'b10,
            "Branch Immediate");

        //--------------------------------------------------------------
        // BEQ
        //--------------------------------------------------------------

        funct3 = 3'b000;
        zero = 1;

        #1;

        check(ctrl_signal[CTRL::PCwrite],
            "BEQ taken");

        zero = 0;

        //--------------------------------------------------------------
        // BNE
        //--------------------------------------------------------------

        funct3 = 3'b001;

        #1;

        check(ctrl_signal[CTRL::PCwrite],
            "BNE taken");

        //--------------------------------------------------------------
        // BLT
        //--------------------------------------------------------------

        funct3 = 3'b100;
        negative = 1;

        #1;

        check(ctrl_signal[CTRL::PCwrite],
            "BLT taken");

        negative = 0;

        //--------------------------------------------------------------
        //==============================================================
        //====================== LUI ================================
        //==============================================================
        //--------------------------------------------------------------

        cpu_reset();

        opcode = IS::LUI;

        step();
        step();

        check(Immtype == 2'b11,
            "LUI immediate");

        //--------------------------------------------------------------
        //==============================================================
        //====================== AUIPC ================================
        //==============================================================
        //--------------------------------------------------------------

        cpu_reset();

        opcode = IS::AUIPC;

        step();
        step();

        check(Immtype == 2'b11,
            "AUIPC immediate");

        //--------------------------------------------------------------
        //==============================================================
        //====================== JAL ================================
        //==============================================================
        //--------------------------------------------------------------

        cpu_reset();

        opcode = IS::JAL;

        step();
        step();

        check(Immtype == 2'b11,
            "JAL immediate");

        //--------------------------------------------------------------
        //==============================================================
        //====================== JALR ================================
        //==============================================================
        //--------------------------------------------------------------

        cpu_reset();

        opcode = IS::JALR;

        step();
        step();

        check(Immtype == 2'b00,
            "JALR immediate");

        //--------------------------------------------------------------
        // Illegal Opcode
        //--------------------------------------------------------------

        cpu_reset();

        opcode = IS::op_code_t'(7'h7F);

        step();

        check(1, "Illegal opcode executed without simulation crash");


        //--------------------------------------------------------------
        
        //--------------------------------------------------------------
        // HALT
        //--------------------------------------------------------------

        cpu_reset();

        opcode = IS::SYSTEM;

        step();

        check(~ctrl_signal[CTRL::HALTED], "Not Halted");

        step();

        check(~ctrl_signal[CTRL::HALTED], "Not Halted");

        step();

        check(ctrl_signal[CTRL::HALTED], "Halted");
        
        step();

        check(ctrl_signal[CTRL::HALTED], "Still Halted");
        
        step();
        
        check(ctrl_signal[CTRL::HALTED], "Yet Still Halted");


        //--------------------------------------------------------------

        $display("");
        $display("===============================================");
        $display("Tests Passed : %0d", passed);
        $display("Tests Failed : %0d", failed);
        $display("===============================================");
        $display("");

        #20;
        $finish;

    end

endmodule
